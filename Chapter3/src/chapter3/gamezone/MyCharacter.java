public class MyCharacter {
    private String color;
    private int eyes;
    private int lives;

    public MyCharacter(String color, int eyes, int lives) {
        this.color = color;
        this.eyes = eyes;
        this.lives = lives;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getEyes() {
        return eyes;
    }

    public void setEyes(int eyes) {
        this.eyes = eyes;
    }

    public int getLives() {
        return lives;
    }

    public void setLives(int lives) {
        this.lives = lives;
    }
    
    
    
    
    
}    
